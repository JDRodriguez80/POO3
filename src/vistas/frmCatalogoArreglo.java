      private void actualizarTabla(){
          try {
              DefaultTableModel model = new DefaultTableModel();
              jtArreglos.setModel(model);
              PreparedStatement ps=null;
              ResultSet rs =null;
              Connection conn = MySQLConection.getConnection();
              String querry=("SELECT idArreglo, nombreArreglo, costoArreglo, tiempoElaboracion, comision FROM catalogoarreglos");
              ps= conn.prepareStatement(querry);
              rs=ps.executeQuery();
              ResultSetMetaData rsm= rs.getMetaData();
              int columnas = rsm.getColumnCount();
              model.addColumn("ID");
              model.addColumn("NOMBRE");
              model.addColumn("COSTO");
              model.addColumn("TIEMPO ELAB.");
              model.addColumn("COMISIÓN");
              int [] anchos = {20,200,50,50,50};
              for(int x=0; x<columnas; x++){
                  jtArreglos.getColumnModel().getColumn(x).setPreferredWidth(anchos[x]);  
              }
              while(rs.next()){
                  Object[] filas = new Object[columnas];
                  for(int i=0; i<columnas; i++){
                      filas[i]=rs.getObject(i+1);
                  }
                  model.addRow(filas);
              }
              
              
          } catch (Exception e) {
              JOptionPane.showMessageDialog(null, "Error al actualizar"+e);
          }
       
       
       } 
        
    
    
    
