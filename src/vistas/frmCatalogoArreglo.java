    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            String querry=("INSERT INTO catalogoarreglos (nombreArreglo, costoArreglo, tiempoElaboracion, comision) values(?,?,?,?)");
            Connection conn = MySQLConection.getConnection();
            PreparedStatement ps = conn.prepareStatement(querry);
            /*--------- calculo de comision----------*/
            double comision;
            double precio= Double.parseDouble(txtCosto.getText());
            comision= precio*.05;
            
            ps.setString(1, txtNombreArreglo.getText());
            ps.setDouble(2, Double.parseDouble(txtCosto.getText()));
            ps.setString(3, txtTiempo.getText());
            ps.setDouble(4, comision);
            int res=ps.executeUpdate();
            
            if(res>0){
                JOptionPane.showMessageDialog(null, "arreglo agregado exitosamente");
               limpiar();
               actualizarTabla();
            }else {JOptionPane.showMessageDialog(null, "error al guardar arreglo");
                limpiar();
            }
            conn.close();
  
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Error de conexion al servidor"+e.getMessage());
            limpiar();
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
            PreparedStatement ps = null;
            try {
                
            int fila = jtArreglos.getSelectedRow();
            String idArreglo = jtArreglos.getValueAt(fila, 0).toString();
            Connection conn = MySQLConection.getConnection();    
            String querry=("DELETE FROM catalogoarreglos WHERE idArreglo=?");
            ps=conn.prepareStatement(querry);
            ps.setString(1, idArreglo);
            ps.execute();
            actualizarTabla();
            limpiar();
            JOptionPane.showMessageDialog(null,"elemento eliminado");
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"error en eliminado"+e);
        }
    }//GEN-LAST:event_btnDelActionPerformed

