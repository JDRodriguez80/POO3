    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
                
        try {
            String querry=("INSERT INTO empleados (nombreEmpleado, user, password, puntoDeVenta, puesto) VALUES(?,?,?,?,?)");
            
            Connection conn = MySQLConection.getConnection();
            PreparedStatement ps = conn.prepareStatement(querry);
            ps.setString(1, txtNombre1.getText());
            ps.setString(2, txtUser.getText());
            ps.setString(3, txtPassword.getText());
            ps.setString(4, txtPuntoVenta.getText());
            ps.setString(5, cmbPuesto.getSelectedItem().toString());
            
            int res=ps.executeUpdate();
            if(res>0){
                JOptionPane.showMessageDialog(null, "Empleado Agregado exitosamente");
                limpiar();
            }else { JOptionPane.showMessageDialog(null, "Error al guardar");
                limpiar();
            }
            conn.close();
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"Error de conexion al servidor"+ex.getMessage());
            limpiar();
        }
        
     
    }//GEN-LAST:event_btnAddActionPerformed

