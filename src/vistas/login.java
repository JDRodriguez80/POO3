    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        operaciones op = new operaciones();
        try {
            String userStr = txtUser.getText();
            String passStr = txtPass.getText();
            String puestoStr= cmbRol.getSelectedItem().toString();
            
            if(operaciones.logeado(userStr, passStr, puestoStr, this)){
                if(puestoStr=="administrador"){
                    new vistaAdmin().setVisible(true);
                    this.dispose();
                }if(puestoStr=="dise√±ador"){
                    new vistaAdmin().setVisible(true);
                    this.dispose();
                }
                   
            }else{
                     JOptionPane.showMessageDialog(null, "Verificar el usaurio/contrase√±a");
                }
        } catch (Exception e) {
             JOptionPane.showMessageDialog(this, "Verifique la informacion");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

